// "2024-01" → "Ocak 2024"
export const formatMonthLabel = (monthKey: string): string => {
  const [year, month] = monthKey.split("-")
  const date = new Date(parseInt(year), parseInt(month) - 1, 1)
  return new Intl.DateTimeFormat("tr-TR", {
    month: "long",
    year: "numeric",
  }).format(date)
}
